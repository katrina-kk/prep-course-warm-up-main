/**
 * Loop over the array and for each alive writer print out the following:
 * "Hi, my name is {firstName} {lastName}. I am {age} years old, and work as a {occupation}."
 */

//constant with types 

interface I_Writter {
    firstName: string;
    lastName: string;
    occupation: string;
    age: number;
    alive: boolean;
}
const writers: I_Writter[] = [
  {
    firstName: "Virginia",
    lastName: "Woolf",
    occupation: "writer",
    age: 59,
    alive: false
  },
  {
    firstName: "Zadie",
    lastName: "Smith",
    occupation: "writer",
    age: 41,
    alive: true
  },
  {
    firstName: "Jane",
    lastName: "Austen",
    occupation: "writer",
    age: 41,
    alive: false
  },
  {
    firstName: "bell",
    lastName: "hooks",
    occupation: "writer",
    age: 64,
    alive: true
  }
];


//forEach used because we don't need to return anything, just go through
//if we would need string to return as new array then .map() would be good option.
writers.forEach((writer) => {
  if (writer.alive) {
 console.log(
  `Hi, my name is ${writer.firstName} ${writer.lastName}. I am ${writer.age} years old, and work as a ${writer.occupation}.`
);
  }
});

